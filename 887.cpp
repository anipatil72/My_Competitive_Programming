ll mulmod(ll a, ll b, ll mod)
{
    ll res = 0; // Initialize result
    a = a % mod;
    while (b > 0)
    {
        // If b is odd, add 'a' to result
        if (b % 2 == 1)
            res = (res + a) % mod;

        // Multiply 'a' with 2
        a = (a * 2) % mod;

        // Divide b by 2
        b /= 2;
    }

    // Return result
    return res % mod;
}

int numSubseq(vector<int> &nums, int target)
{
    int n = nums.size();

    sort(nums.begin(), nums.end());

    // dbg(nums);

    if (target < nums[0])
    {
        return 0;
    }

    vector<long long> power(n);

    power[0] = 1;

    for (int i = 1; i < n; ++i)
    {
        power[i] = (power[i - 1] * 2) % MOD;
    }

    long long ans = 0;

    for (int i = 0; i < n; i++)
    {
        long long left = nums[i];

        long long pos = (long long)(upper_bound(nums.begin(), nums.end(), target - left) - nums.begin());

        pos--;

        if (pos < i)
        {
        }
        else
        {
            long long x = power[pos - i];
            ans += (x);

            ans %= MOD;
            // dbg(i, pos, x, ans);
        }
    }

    return ans;
}

