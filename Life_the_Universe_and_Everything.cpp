#include <bits/stdc++.h>
#define ll long long
#define M 1000000007
using namespace std;

int main()
{

    vector<int> a;

    while (true)
    {

        int d;

        cin >> d;

        if (d != 42)
        {
            a.push_back(d);
        }
        else
        {
            break;
        }
    }

    for (int i = 0; i < a.size(); i++)
    {
        cout << a[i] << "\n";
    }

    return 0;
}

