map<long long, long long> factorize(long long n)
{
    map<long long, long long> ans;
    for (long long i = 2; i * i <= n; i++)
    {
        while (n % i == 0)
        {
            ans[i]++;
            n /= i;
        }
    }
    if (n > 1)
    {
        ans[n]++;
        n = 1;
    }
    return ans;
}

long long int primeProduct(int N)
{
    int64_t p = 1;

    map<long long, long long> ans = factorize(N);

    for (auto x : ans)
    {
        p *= x.first;
    }

    return p;
}

